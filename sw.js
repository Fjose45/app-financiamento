// Notificações push
self.addEventListener('push', (event) => {
  const options = {
    body: event.data ? event.data.text() : 'Nova atualização disponível!',
    icon: '/moeda_192.png',
    badge: '/moeda_192.png',
    vibrate: [100, 50, 100],
    data: {
      dateOfArrival: Date.now(),
      primaryKey: 1
    },
    actions: [
      {
        action: 'explore',
        title: 'Abrir App',
        icon: '/moeda_192.png'
      },
      {
        action: 'close',
        title: 'Fechar',
        icon: '/moeda_192.png'
      }
    ]
  };

  event.waitUntil(
    self.registration.showNotification('Sistema de Financiamentos', options)
  );
});

// Clique em notificação
self.addEventListener('notificationclick', (event) => {
  event.notification.close();

  if (event.action === 'explore') {
    event.waitUntil(
      clients.openWindow('/')
    );
  }
});
